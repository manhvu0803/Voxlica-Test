		private int PostProcesses = 0;

		private Material usedmaterial;
		private List<int> WidthTable;
		private int currentWidth;
		private static int activeHulls = 0;

		protected override void Initialize()
		{
			MaximumWidth = Mathf.NextPowerOfTwo(MaximumWidth);

			WidthTable = new List<int>();
			int resolution = MaximumWidth;
			while (resolution > 1)
			{
				for (int i = 0; i < ResolutionFalloff; i++)
				{
					WidthTable.Add(resolution);
				}
				resolution /= 2;
			}


			activeHulls++;

			if(TextureDimensionUV3 >= engine.Data.Length)
			{
				Debug.LogError("This does not work! The texture wants to use voxel dimension " + TextureDimensionUV3 + " which is outside the set dimension count. Increase the dimension count paramter from the Voxel Engine");
				return;
			}

			isInitialized = true;
			m_MeshModJobs = new MarchingCubes_Transvoxel_Calculation[NumCores];
			m_JobHandles = new JobHandle[NumCores];
			m_JobFree = new bool[NumCores];
			for (int i = 0; i < m_JobFree.Length; i++)
			{
				m_JobFree[i] = true;
			}

			workIndex = new int[NumCores];


			CreateMarchingCubeInformation();

			
			for (int i = 0; i < m_MeshModJobs.Length; i++)
			{
				m_MeshModJobs[i].Init();

				m_MeshModJobs[i].Shrink = Shrink;
				

				m_MeshModJobs[i].VertexOffset = VertexOffset;
				m_MeshModJobs[i].EdgeConnection = EdgeConnection;
				m_MeshModJobs[i].EdgeDirection = EdgeDirection;
				m_MeshModJobs[i].CubeEdgeFlags = CubeEdgeFlags;
				m_MeshModJobs[i].TriangleConnectionTable = TriangleConnectionTable;

				m_MeshModJobs[i].data = engine.Data[0];

				if (TextureDimensionUV3 != -1 && TextureDimensionUV3 < engine.Data.Length)
				{
					m_MeshModJobs[i].texturedata_UV3 = engine.Data[TextureDimensionUV3];
				}

				if (TextureDimensionUV4 != -1 && TextureDimensionUV4 < engine.Data.Length)
				{
					m_MeshModJobs[i].texturedata_UV4 = engine.Data[TextureDimensionUV4];
				}

				if (TextureDimensionUV5 != -1 && TextureDimensionUV5 < engine.Data.Length)
				{
					m_MeshModJobs[i].texturedata_UV5 = engine.Data[TextureDimensionUV5];
				}

				if (TextureDimensionUV6 != -1 && TextureDimensionUV6 < engine.Data.Length)
				{
					m_MeshModJobs[i].texturedata_UV6 = engine.Data[TextureDimensionUV6];
				}
			}


			int piececount = Cell_Subdivision * Cell_Subdivision * Cell_Subdivision;
			Haschset = new bool[piececount];
			works = new int[piececount];
			skips = new int[piececount];
			VoxelMeshes = new List<VoxelPiece>(piececount);
			for (int i = 0; i < piececount; i++)
			{
				VoxelMeshes.Add(CreateVoxelPiece("__VOXELPIECE", VoxelMaterial));
			}





		}

		public override void Rebuild()
		{
			for (int index = 0; index < Cell_Subdivision * Cell_Subdivision * Cell_Subdivision; index++)
			{
				WorkerQueue.Enqueue(index);
				Haschset[index] = true;
			}

			if (PostProcesses < 3)
			{
				PostProcesses++;
			}
		}

		public override void RootNodeChanged(NativeVoxelNode node)
		{
			float cellSize = engine.RootSize / Cell_Subdivision;
			float nodesize = engine.Data[0].SizeTable[node.Depth];

			Vector3 cellpos = new Vector3(node.X % cellSize, node.Y % cellSize, node.Z % cellSize);
			float cellextent = nodesize * 0.6f;

			Vector3Int cellRanges_min = new Vector3Int(0, 0, 0);
			Vector3Int cellRanges_max = new Vector3Int(0, 0, 0);


			cellRanges_max.x = (int)((node.X + cellextent) / cellSize);
			cellRanges_max.y = (int)((node.Y + cellextent) / cellSize);
			cellRanges_max.z = (int)((node.Z + cellextent) / cellSize);

			cellRanges_min.x = (int)((node.X - cellextent) / cellSize);
			cellRanges_min.y = (int)((node.Y - cellextent) / cellSize);
			cellRanges_min.z = (int)((node.Z - cellextent) / cellSize);

			int lenght = VoxelMeshes.Count;

			int count = 0;
			for (int x = cellRanges_min.x; x <= cellRanges_max.x; x++)
			{
				for (int y = cellRanges_min.y; y <= cellRanges_max.y; y++)
				{
					for (int z = cellRanges_min.z; z <= cellRanges_max.z; z++)
					{
						int ci = x;
						int cj = y;
						int ck = z;

						int index = ci + Cell_Subdivision * (cj + Cell_Subdivision * ck);

						if (index >= 0 && index < lenght)
						{
							if (!Haschset[index])
							{
								Haschset[index] = true;
								WorkerQueue.Enqueue(index);
								count++;
							}
						}
					}
				}
			}
		}


        protected override void cleanup()        
		{
			for (int i = 0; i < VoxelMeshes.Count; i++)
			{
				if (VoxelMeshes[i])
					DestroyImmediate(VoxelMeshes[i].gameObject);
			}
			VoxelMeshes.Clear();

			WorkerQueue = new Queue<int>();

			if (m_MeshModJobs != null)
			{
				for (int i = 0; i < m_MeshModJobs.Length; i++)
				{
					m_JobHandles[i].Complete();
					m_MeshModJobs[i].CleanUp();

				}
			}
			
			m_JobFree = new bool[0];
			m_MeshModJobs = new MarchingCubes_Transvoxel_Calculation[0];

			

			PostProcesses = 0;

			if (isInitialized)
			{
				activeHulls--;
				if (activeHulls <= 0)
				{
					DisposeMarchingCubeInformation();
				}
			}
			  
			isInitialized = false;
		}

		public override void HideMeshes()
		{
			for (int i = 0; i < VoxelMeshes.Count; i++)
			{
				VoxelMeshes[i].meshfilter.sharedMesh.Clear();
				VoxelMeshes[i].gameObject.SetActive(false);
				
			}
		}

		public override void ShowMeshes()
		{
			for (int i = 0; i < VoxelMeshes.Count; i++)
			{
				
				VoxelMeshes[i].gameObject.SetActive(true);			
			}
		}


		public override void OnDuplicate()
		{
			VoxelPiece[] Pieces = GetComponentsInChildren<VoxelPiece>();
			for (int i = 0; i < Pieces.Length; i++)
			{
				if (Pieces[i].meshfilter.sharedMesh != null)
					Pieces[i].meshfilter.sharedMesh = Instantiate(Pieces[i].meshfilter.sharedMesh);
			}
			VoxelMeshes = new List<VoxelPiece>();
			VoxelMeshes.AddRange(Pieces);
		}

		protected override float GetChecksum()
		{
			return Cell_Subdivision * 1000 + MaximumWidth * 100 + NumCores * 10 + UVPower + SmoothAngle;
		}
	
		public override bool IsWorking()
		{
			if (m_JobFree == null) return false;
			for (int i = 0; i < m_JobFree.Length; i++)
			{
				if (!m_JobFree[i]) return true;
			}

			if (WorkerQueue != null)
			{
				if (WorkerQueue.Count > 0) return true;
			}

			return false;
		}		

		public override void PrepareWorks()
		{
			IsIdle = true;
			if (!isInitialized) return;
			if (!engine) return;
			if (m_MeshModJobs == null)
			{
				engine.CleanUp();
				return;
			}		

			if (usedmaterial == null || usedmaterial != VoxelMaterial)
			{
				if (VoxelMeshes != null)
				{
					for (int i = 0; i < VoxelMeshes.Count; i++)
					{
						if (VoxelMeshes[i])
							VoxelMeshes[i].meshrenderer.sharedMaterial = VoxelMaterial;
					}
				}
				usedmaterial = VoxelMaterial;
			}

			currentWidth = WidthTable[Mathf.Clamp(engine.LODDistance, 0, WidthTable.Count)];



			for (int m = 0; m < m_MeshModJobs.Length; m++)
			{
				if (m_JobFree[m] == false)
				{
					IsIdle = false;
					continue;
				}
				if (WorkerQueue.Count == 0)
				{
					workIndex[m] = -1;
					continue;
				}

				IsIdle = false;

				int index = WorkerQueue.Dequeue();
				
				workIndex[m] = index;
			

				int i = index % Cell_Subdivision;
				int j = (index - i) / Cell_Subdivision % Cell_Subdivision;
				int k = ((index - i) / Cell_Subdivision - j) / Cell_Subdivision;
				float cellSize = engine.RootSize / Cell_Subdivision;
				float startX = i * cellSize;
				float startY = j * cellSize;
				float startZ = k * cellSize;
				m_MeshModJobs[m].SetWidth(currentWidth);
				m_MeshModJobs[m].voxelSize = cellSize / (currentWidth);
				m_MeshModJobs[m].cellSize = cellSize;
				m_MeshModJobs[m].positionoffset = new Vector3(startX, startY, startZ);
				m_MeshModJobs[m].SmoothingAngle = SmoothAngle;
				m_MeshModJobs[m].minimumID = MinimumID;
				m_MeshModJobs[m].maximumID = MaximumID;
				m_MeshModJobs[m].UVPower = UVPower;
				m_JobHandles[m] = m_MeshModJobs[m].Schedule();
				m_JobFree[m] = false;

			}

			if(IsIdle && PostProcesses > 0)
			{
				for (int i = 0; i < works.Length; i++)
				{
					if (NoPostProcess)
					{
						works[i] = 0;
					}
					else if (works[i] > 0)
					{						
						if (!Haschset[i])
						{
							Haschset[i] = true;
							WorkerQueue.Enqueue(i);
							works[i]--;
						}
					}
				}
			
				PostProcesses--;

				if(NoPostProcess)
				{
					PostProcesses = 0;
					NoPostProcess = false;
				}
				
			}
			

		}

		public override void CompleteWorks()
		{
			if (!isInitialized) return;
			if (!engine) return;

			for (int m = 0; m < m_MeshModJobs.Length; m++)
			{
				if (workIndex[m] == -1) continue;
				if (!m_JobHandles[m].IsCompleted && skips[m] < 3)
				{
					skips[m]++;
					continue;
				}


				m_JobHandles[m].Complete();
				skips[m] = 0;

				int index = workIndex[m];

				VoxelPiece piece = VoxelMeshes[index];

				piece.Clear();
				MarchingCubes_Transvoxel_Calculation usedcalculator = m_MeshModJobs[m];
				if (usedcalculator.verticeArray.Length != 0)
				{
					piece.SetVertices(usedcalculator.verticeArray);
					piece.SetTriangles(usedcalculator.triangleArray);
					piece.SetNormals(usedcalculator.normalArray);
					piece.SetTangents(usedcalculator.tangentsArray);
					piece.SetUVs(0, usedcalculator.uvArray);

					if (TextureDimensionUV3 != -1)
						piece.SetUVs(2, usedcalculator.uv3Array);

					if (TextureDimensionUV4 != -1)
						piece.SetUVs(3, usedcalculator.uv4Array);

					if (TextureDimensionUV5 != -1)
						piece.SetUVs(4, usedcalculator.uv5Array);

					if (TextureDimensionUV6 != -1)
						piece.SetUVs(5, usedcalculator.uv6Array);

					if (AddTesselationColors)
					{
						piece.SetColors(m_MeshModJobs[m].colorArray);
					}
				}

				m_JobFree[m] = true;

				piece.voxelMesh.RecalculateBounds();


				piece.EnableCollision(currentWidth == MaximumWidth && !NoCollision);
				
					


				Haschset[index] = false;


			}
		}

		protected override void setRegionsDirty(VoxelRegion region)
		{
			int lenght = VoxelMeshes.Count;
			if (lenght == 1)
			{
				if (!Haschset[0])
				{
					Haschset[0] = true;
					WorkerQueue.Enqueue(0);
					works[0] = 3;
				}

			}
			else
			{
				float cellSize = engine.RootSize / Cell_Subdivision;

				Vector3Int cellRanges_min = new Vector3Int(0, 0, 0);
				Vector3Int cellRanges_max = new Vector3Int(0, 0, 0);

				cellRanges_max.x = (int)((region.RegionMax.x) / cellSize);
				cellRanges_max.y = (int)((region.RegionMax.y) / cellSize);
				cellRanges_max.z = (int)((region.RegionMax.z) / cellSize);

				cellRanges_min.x = (int)((region.RegionMin.x) / cellSize);
				cellRanges_min.y = (int)((region.RegionMin.y) / cellSize);
				cellRanges_min.z = (int)((region.RegionMin.z) / cellSize);

				for (int x = cellRanges_min.x; x <= cellRanges_max.x; x++)
				{
					for (int y = cellRanges_min.y; y <= cellRanges_max.y; y++)
					{
						for (int z = cellRanges_min.z; z <= cellRanges_max.z; z++)
						{
							int ci = x;
							int cj = y;
							int ck = z;

							int index = ci + Cell_Subdivision * (cj + Cell_Subdivision * ck);

							if (index >= 0 && index < lenght)
							{
								if (!Haschset[index])
								{
									Haschset[index] = true;
									WorkerQueue.Enqueue(index);
									works[index] = 3;
								}


							}

						}
					}
				}
			}


			if (PostProcesses < 3)
			{
				PostProcesses++;
			}
		}

		public override void UpdateLOD(int newLOD)
		{
			int oldLOD = engine.LODDistance;

			int oldwidth = WidthTable[oldLOD];
			int newwidth = WidthTable[newLOD];
			
			if(oldwidth != newwidth)
			{
				Rebuild();
			}

			engine.HullsDirty = true;
		}

